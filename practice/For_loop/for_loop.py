# ex.list state
fruits = ["apple", "banana", "cherry"]
for i in fruits:
    print(i)

# ex. string statement
fruits ="Apple"
for i in fruits:
    print(i)

# ex.Break Statement
fruits = ["apple", "banana", "cherry"]
for i in fruits:
    if i == "banana":
        break
    print(i)

# ex.continue Statement
fruits = ["apple", "banana", "cherry"]
for i in fruits:
    if i == "banana":
        continue
    print(i)

# ex. Pass Statement
fruits = ["apple", "banana", "cherry"]
for i in fruits:
    if i == "banana":
        pass
    print(i)

# ex.Range function
for i in range(10):
    print(i)



year = int(input("enter number:-"))

if year == 1:
    print(1,"= ")
elif year  == 2:
    print(2,"= Tuesday")
elif year == 3:
    print(3, "= Wednesday") 
elif year == 4:
    print(4, "= Thursday")
elif year == 5:
    print(5, "= Friday")
elif year == 6:
    print(6, "= Saturday")
elif year == 7:
    print(7, "=Sunday  ")
else:
    print("wrong choice")         
week = int(input("enter number:-"))

if week == 1:
    print(1,"= Monday")
elif week == 2:
    print(2,"= Tuesday")
elif week == 3:
    print(3, "= Wednesday") 
elif week == 4:
    print(4, "= Thursday")
elif week == 5:
    print(5, "= Friday")
elif week == 6:
    print(6, "= Saturday")
elif week == 7:
    print(7, "=Sunday  ")
else:
    print("wrong choice")           